    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        PostgraduateExam pe1=new PostgraduateExam();
        
        pe1.setSemester(Integer.parseInt(semesterIdText.getText()));
        pe1.setPostgraduateId(postgraduateIdText.getText());
        pe1.setMarks(Integer.parseInt(marksText.getText()));
        pe1.setSubjectCode(Integer.parseInt(subjectCodeText.getText()));
        pe1.setExamPercentage(Double.parseDouble(percentageText.getText()));
  
        
        
        boolean result=dbsu.addPostgraduateExam(pe1);
             if(result==true){
            JOptionPane.showMessageDialog(this, "Successfully Inserteted");
            //undergraduateClear();
            this.dispose();   //Jframe will dissapear after we click ok on the message
        }else{
            JOptionPane.showMessageDialog(this, "Sorry an error  occured while Inserting");
        }
    }//GEN-LAST:event_submitButtonActionPerformed

